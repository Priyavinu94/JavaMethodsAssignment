package theoryQuestion8;

public class BasicMathOperation2 {

	double num1 = 0;
	double num2 = 0;
	double sum = num1 + num2;
	double diff = num1 - num2;
	
	void setSum(double num1, double num2) {
		sum = num1 + num2;
	}
	
	void setDiff(double num1, double num2) {
		diff = num1 - num2;
	}
	
	double getSum() {
		return sum;
	}
	
	double getDiff() {
		return diff;
	}
	
}
